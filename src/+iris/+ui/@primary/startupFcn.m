%   '    }', ...
%   '  ]'] ...
%   );
obj.window.executeJS( ...
  sprintf( ...
    'var ax = new IrisAxes(%s,parentNode.lastChild,%s);', ...
    fileread('irisPlot.json'), ...
    jsonencode(obj.layout) ...
    ) ...
  );
obj.window.executeJS('ax.init();');
%}

%%% END D3.js
%% Keyboard
v = ver('matlab');
if str2double(v.Version) < 9.5

  if strcmpi(obj.window.executeJS('keyPressed === ''undefined'''),'true')
    obj.executeJSFile(JSScripts{3});
  end
  [~,bKey] = mlapptools.getWebElements(obj.KeyboardButton);
  obj.window.executeJS( ...
    sprintf([ ...
      'var keyData;', ...
      'var button = document.querySelector("[%s = ''%s'']");', ...
      'document.addEventListener("keydown", (e) => {', ...
      'if ( ["Control","Alt","Meta","Shift"].indexOf(e.key) > -1 ) return;', ...
      'keyPressed(e).then((resolved) => {keyData = resolved;', ...
      'button.click();})', ...
      '});' ...
    ], ...
    bKey.ID_attr,bKey.ID_val));
else
  obj.container.WindowKeyPressFcn = @obj.KeypressCapture;
end
%% Add the listener for the selection
addlistener(obj,'selection', 'PostSet',@(s,e)obj.onSelectionUpdate);

%% Tooltips

objects = {'ScaleSwitch','BaselineSwitch','FilterSwitch','EpochSwitch'};
directions = {'right', 'up','up','left'};
for o = 1:length(objects)
  tip = Aes.strLib(objects{o});
  pause(0.1);
  try
    addTooltip(obj.(objects{o}), tip, directions{o});
  catch
    %%% log this
  end
end

%% Cleanup

end

