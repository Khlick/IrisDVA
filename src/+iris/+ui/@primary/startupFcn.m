%   '  }, ', ...
%   '  "xaxis": {', ...
%   '    "title": "X", ', ...
%   '    "zerolinecolor": "rgba(174, 174, 174,1)",', ...
%   '    "zeroline": true,', ...
%   '    "scale": "linear",', ...
%   '    "grid": true', ...
%   '  }, ', ...
%   '  "font": {', ...
%   '    "family": "Times New Roman", ', ...
%   '    "size": 16', ...
%   '  }, ', ...
%   '  "margin": {', ...
%   '    "r": 15, ', ...
%   '    "b": 40, ', ...
%   '    "l": 60, ', ...
%   '    "t": 15', ...
%   '  }', ...
%   '}'] ...
%   ); 
%

% Example empty data obj
% obj.window.executeJS(['var datArr = [', ...
%   '    {', ...
%   '      "line": {', ...
%   '        "color":"rgb(50,135,200)",', ...
